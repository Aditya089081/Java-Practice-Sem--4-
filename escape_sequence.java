public class escape_sequence {
    public static void main(String[] args) {
        // we are learning escape sequences in java.
        System.out.print("\n"); //used for escaping or shifting line.
        System.out.println("\b ");
        System.out.println("/t");
        System.out.println("\\n");
        System.out.println("\"//");
                //   \ --> for escaping the working og predefined.
                //   \n --> we use \ not /.
    }
}
