public class if_elseif_else {
    public static void main(String[] arg){
        int a = 23;
        if(a<=10){
            System.out.println("A is lesser than or equal to 10.");
        }
        else if(a<=50){
            System.out.println("A is lesser than 50.");
        }
        else{
            System.out.println("A is greater than 50.");
        }
    }
}
