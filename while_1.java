public class while_1 {
    // Printing Stars
    /**Welcome to Aditya's World.
     * @param arg
     */
    public static void main(String[] arg){
        int a = 5;
        int i =1;
        while(i<=a){
                int j = 0;
                while(j<i){
                    System.out.print("* ");
                    j++;
                }
            i++;
            System.out.print("\n");
        }

    }
}
